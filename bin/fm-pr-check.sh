#!/usr/bin/env bash
# Record a PR-ready task: appends pr=<url> and a verified pr_head=<sha> to
# state/<id>.meta when available, then arms the watcher's PR-health poll by writing
# state/<id>.check.sh (the watcher's check contract: output = wake firstmate,
# silence = keep sleeping). The poll now watches the whole health of a ready PR,
# not just its merge: it wakes firstmate once per transition into `merged`,
# `conflicted: ...` (mergeStateStatus went DIRTY - a real conflict, the classic
# parallel-PR collision on shared files), or `checks-red: <check>` (a required
# check turned terminally red). Edge-triggered via state/<id>.pr-health so a bad PR
# wakes once, not every poll; silent on transient/unknown states and on gh errors.
# The probe/classify logic lives in bin/fm-pr-health-lib.sh.
# Usage: fm-pr-check.sh <task-id> <pr-url>
set -eu

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
FM_ROOT="${FM_ROOT_OVERRIDE:-$(cd "$SCRIPT_DIR/.." && pwd)}"
FM_HOME="${FM_HOME:-${FM_ROOT_OVERRIDE:-$FM_ROOT}}"
STATE="${FM_STATE_OVERRIDE:-$FM_HOME/state}"
"$FM_ROOT/bin/fm-guard.sh" || true
ID=$1
URL=$2

META="$STATE/$ID.meta"
if [ -f "$META" ]; then
  WT=$(grep '^worktree=' "$META" | tail -1 | cut -d= -f2- || true)
  LOCAL_HEAD=
  PR_HEAD=
  if [ -n "$WT" ] && [ -d "$WT" ]; then
    LOCAL_HEAD=$(git -C "$WT" rev-parse --verify HEAD 2>/dev/null || true)
    if [ -n "$LOCAL_HEAD" ] && command -v gh >/dev/null 2>&1; then
      if REMOTE_HEAD=$(cd "$WT" && gh pr view "$URL" --json headRefOid -q .headRefOid 2>/dev/null); then
        if [ "$LOCAL_HEAD" = "$REMOTE_HEAD" ]; then
          PR_HEAD=$LOCAL_HEAD
        fi
      fi
    fi
  fi
  if ! grep -qxF "pr=$URL" "$META"; then
    echo "pr=$URL" >> "$META"
  fi
  if [ -n "$PR_HEAD" ] && ! grep -qxF "pr_head=$PR_HEAD" "$META"; then
    echo "pr_head=$PR_HEAD" >> "$META"
  fi
fi

HEALTH_LIB="$FM_ROOT/bin/fm-pr-health-lib.sh"
HEALTH_FILE="$STATE/$ID.pr-health"

# The generated check sources the shared health library and delegates to its
# edge-triggered emit. The absolute lib path and the PR URL are baked in at
# generation time; a missing lib fails safe (exit 0, no output).
cat > "$STATE/$ID.check.sh" <<EOF
# PR-health poll for $ID (generated by fm-pr-check.sh). Wakes firstmate once per
# transition into merged / conflicted / checks-red; silent on unchanged, transient,
# or unknown states and on any gh error. Last category persisted in the health file.
. "$HEALTH_LIB" 2>/dev/null || exit 0
fm_pr_health_emit "$URL" "$HEALTH_FILE"
EOF
echo "armed: state/$ID.check.sh polls $URL (merge + conflict + red-check health)"
